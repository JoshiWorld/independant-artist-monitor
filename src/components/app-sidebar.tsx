export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const [isSearching, setIsSearching] = React.useState(false);
  const [query, setQuery] = React.useState("");
  const [debouncedQuery] = useDebounce(query, 300);

  const { data: user } = api.user.getInfoForDashboard.useQuery();
  const { data: adAccounts, isLoading: isLoadingAdAccounts } = api.user.getAdAccounts.useQuery({ name: debouncedQuery.length > 0 ? debouncedQuery : undefined });

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <Link href="/">
                <Image
                  src="/images/ia-logo.png"
                  alt="logo"
                  width={30}
                  height={30}
                />
                <span className="text-base font-semibold">IndependentArtists</span>
              </Link>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavDocuments items={adAccounts} isLoading={isLoadingAdAccounts} />
        <NavSecondary setIsSearching={setIsSearching} setQuery={setQuery} query={query} isSearching={isSearching} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        {user ? <NavUser user={user} /> : <LoadingDots />}
      </SidebarFooter>
    </Sidebar>
  )
}
